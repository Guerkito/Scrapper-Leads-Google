def generate_grid(city: str, grid_n: int = 3, zoom: int = 14):
    """
    Genera una lista de strings "coord:lat,lng,zoom" formando una grilla
    de grid_n × grid_n puntos centrada en la ciudad.

    Paso fijo de 0.018° ≈ 2 km entre puntos.
    Para grid_n=3 → cubre ~4 km de radio.
    Para grid_n=5 → cubre ~7 km de radio.
    Para grid_n=7 → cubre ~10 km de radio.

    Retorna None si la ciudad no está en el diccionario.
    """
    if city not in CITY_COORDS:
        return None

    lat, lng = CITY_COORDS[city]
    step = 0.018  # ~2 km entre puntos
    offset = step * (grid_n - 1) / 2

    points = []
    for i in range(grid_n):
        for j in range(grid_n):
            plat = round(lat - offset + i * step, 5)
            plng = round(lng - offset + j * step, 5)
            points.append(f"coord:{plat},{plng},{zoom}")

    return points
